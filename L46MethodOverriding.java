package l46;

// method overriding = when a subclass provides its own implementation of a method that is already defined
public class L46MethodOverriding {

	public static void main(String[] args) {
		// creating new objects
		Dog dog = new Dog();
		Cat cat = new Cat();
		Fish fish = new Fish();
		
		dog.move();
		cat.move();
		fish.move();
		
			

	}

}
